package ResponseStruct;
public class EmojiStruct {
    private String id;
    private String name;
    private String code;
    private String imageUrl;
    private boolean isAnimated;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }

    public boolean isAnimated() {
        return isAnimated;
    }

    public String getImageUrl() {
        return imageUrl;
    }
}
